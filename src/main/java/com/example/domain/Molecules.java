package com.example.domain;

public class Molecules {
	
	private String name; 
	private String chemical_formula;
	private Double density;
	private Double average_melting_point;
	private Double acidity;
	private String hazard_statements;
	private String note;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getChemical_formula() {
		return chemical_formula;
	}
	public void setChemical_formula(String chemical_formula) {
		this.chemical_formula = chemical_formula;
	}
	public Double getDensity() {
		return density;
	}
	public void setDensity(Double density) {
		this.density = density;
	}
	public Double getAverage_melting_point() {
		return average_melting_point;
	}
	public void setAverage_melting_point(Double average_melting_point) {
		this.average_melting_point = average_melting_point;
	}
	public Double getAcidity() {
		return acidity;
	}
	public void setAcidity(Double acidity) {
		this.acidity = acidity;
	}
	public String getHazard_statements() {
		return hazard_statements;
	}
	public void setHazard_statements(String hazard_statements) {
		this.hazard_statements = hazard_statements;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	
	

}
